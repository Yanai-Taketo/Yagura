namespace Yagura.Ingestion.Diagnostics;

/// <summary>
/// スプール退避の契機（architecture.md §3.2.1 の 2 形態 + 停止時ドレイン。Issue #271 / M-7）。
/// <c>yagura.ingestion.spool.evacuated</c> カウンタの <c>reason</c> タグ値になる。
/// </summary>
/// <remarks>
/// M-7 の残作業（§5.3・§9）だった「退避契機の形態（Q2 溢れ / タイムアウト）が現行実装では
/// 観測不能」を埋めるための次元。<b>本タグは可観測性（契機分布の把握）のためのものであり、
/// 昇格案内の誤発火防止の判定（§5.3）を変えるものではない</b>——§5.3 の分類は退避形態ではなく
/// 「同一観測窓内に成功した書き込みが存在するか」で行う設計であり、その方針は据え置く。
/// </remarks>
public enum SpoolEvacuationReason
{
    /// <summary>
    /// 容量形態: Q2 が満杯で新規投入分を受け取れず退避した（持続的な速度不足はこの形で現れる。
    /// §3.2.1。解析段の実行時経路）。
    /// </summary>
    Q2Overflow,

    /// <summary>
    /// 時間形態: 永続化バッチの DB 書き込みが失敗した、またはタイムアウトで打ち切られて退避した
    /// （§3.2.1。永続化段の実行時経路。書き込みゲート取得タイムアウトを含む）。
    /// </summary>
    WriteTimeout,

    /// <summary>
    /// 停止時ドレイン: サービス停止要求により DB へ書き切るのを待たず退避した（速度不足でも
    /// 障害でもない計画退避。§1.3 手順 2）。飽和シグナルとして数えるべきでない退避を切り分ける。
    /// </summary>
    Shutdown,
}
